import UIKit

extension UIFont {
    func montserratExtraBold(with size: CGFloat) -> UIFont {
        UIFont(name: "Montserrat-ExtraBold", size: size)!
    }

    func montserratBold(with size: CGFloat) -> UIFont {
        UIFont(name: "Montserrat-Bold", size: size)!
    }

    func montserratRegular(with size: CGFloat) -> UIFont {
        UIFont(name: "Montserrat-Regular", size: size)!
    }

    func montserratLight(with size: CGFloat) -> UIFont {
        UIFont(name: "Montserrat-Light", size: size)!
    }

    func robotoBold(with size: CGFloat) -> UIFont {
        UIFont(name: "Roboto-Bold", size: size)!
    }

    func robotoRegular(with size: CGFloat) -> UIFont {
        UIFont(name: "Roboto-Regular", size: size)!
    }
}
